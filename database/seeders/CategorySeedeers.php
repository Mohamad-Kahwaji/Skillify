                'شركة استيراد وتصدير',
                'شركة خدمات',
            ],
        ];

        foreach ($categories as $typeName => $items) {
            $type = ActiveTypebusiness::firstWhere('name', $typeName);
            if (!$type) continue;

            foreach ($items as $name) {
                Category::firstOrCreate(
                    ['name' => $name, 'active_typebusiness_id' => $type->id]
                );
            }
        }
    }
}
