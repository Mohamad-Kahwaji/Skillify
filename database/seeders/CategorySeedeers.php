            // ── حرفة ────────────────────────────────────
            'حرفة' => [
                'البناء والتشييد',
                'الكهرباء والسباكة',
                'النجارة والأثاث',
                'الحدادة والمعادن',
                'الخياطة والنسيج',
                'الطباخة والحلويات',
                'الزراعة والبستنة',
                'التجميل والعناية',
